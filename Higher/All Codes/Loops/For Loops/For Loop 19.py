# List of first names
firstNames = ['John', 'Sarah', 'Bob']

# List of corresponding ages
ages = [13, 32, 43]

# Loop through the range of the length of the firstNames list
for count in range(len(firstNames)):
    # Print the first name at the current index
    print(firstNames[count])
    
    # Print the corresponding age at the current index
    print(ages[count])
