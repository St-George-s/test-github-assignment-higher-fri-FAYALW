# Lists to hold names and their corresponding ages
names = ["Bob", "Sally", "Lucy"]  # List of names
ages = [4, 5, 23]  # List of corresponding ages

# Loop through the range of the length of the names list
for count in range(len(names)):
    print(names[count])  # Print the name at the current index
    print(ages[count])   # Print the age at the same index
